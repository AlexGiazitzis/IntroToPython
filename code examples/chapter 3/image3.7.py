age = 13

# Multiple if branches under one construct
# only the statements in the body of the first
# condition to evaluate to true will be executed
if age >= 18 and age <= 23:
    print("Young adult")
elif age >= 24 and age <= 59:
    print("Adult")
elif age >= 60:
    print("Elder")
elif age >= 8 and age <= 17:
    printf("Kid")
