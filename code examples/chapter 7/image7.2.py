import math as m
# similarly to the from - import statement
# a file/module imported with the import statement,
# can be given an alias

c = m.cos(m.pi)
print(c)

s = m.sim(m.pi / 4)
print(s)

sqrt2 = m.sqrt(2)
print(sqrt2)

a = m.pow(9, 1/3)
print(a)

b = m.log(1)
print(b)

d = m.factorial(5)
print(d)
